function select(elem) {
   return document.querySelector(elem);
}

let curr = 0;
let isAnimating = false;

function setData(index){
   select(".prflimg img").src = users[index].profilePic;
   select(".badge h5").textContent = users[index].pendingMessage;
   select(".location h3").textContent = users[index].location;
   select(".name h1:nth-child(1)").textContent = users[index].name;
   select(".name h1:nth-child(2)").textContent = users[index].age;

   var clutter = "";
   users[index].intersts.forEach(function (interest) {
      clutter += `<div class="tag flex items-center bg-white/30 px-3 py-1 rounded-full   gap-3"> ${interest.icon}
    
    <h3 class="text-sm text-lfg  tracking-tighter capitalized">${interest.interest}</h3>
    </div>`;

   });
   select(".tags").innerHTML = clutter;

   select(".bio p").textContent = users[index].bio;

}

(function setInitial() {
   select(".maincard img").src = users[curr].displayPic;
   select(".incomingcard img").src = users[curr + 1].displayPic;
  
   setData(curr);
   curr = 2;
})();



function imageChange() {
   if (!isAnimating) {
      isAnimating = true;



      let tl = gsap.timeline({
         onComplete: function () {
            isAnimating = false;
            let main = select(".maincard");
            let incoming = select(".incomingcard");

            incoming.classList.remove("z-[2]");
            incoming.classList.add("z-[3]");
            incoming.classList.remove("incomingcard");

            main.classList.remove("z-[3]");
            main.classList.add("z-[2]");
            gsap.set(main, {
               scale: 1,
               opacity: 1

            })
            if (curr === users.length) curr = 0;
            select(".maincard img").src = users[curr].displayPic;
            
            curr++;
            main.classList.remove("maincard");
            incoming.classList.add("maincard");
            main.classList.add("incomingcard");
         }
      });
      tl.to(".maincard", {
         scale: 1.1,
         opacity: 0,
         ease: Circ,
         duration: .9
      }, "a")
         .from(".incoming", {
            scale: .9,
            opacity: 0,
            ease: Circ,
            duration: 1.1
         }, "a")
   }

}
